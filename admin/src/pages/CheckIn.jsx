// ── Shared Hardware Concurrency Lock ─────────────────────────────────────────
// Ensures async start/stop operations are executed strictly in order.
// This completely avoids camera conflicts and resource locks on double renders.
let globalScannerInstance = null;
let globalScannerPromise = Promise.resolve();

// ── Utility ────────────────────────────────────────────────────────────────
const parseQRToken = (raw) => {
  try {
    const data = JSON.parse(raw);
    // Validate required fields
    if (!data.memberId && !data.id) throw new Error('Invalid token: no member ID');
    
    const now = Date.now();
    const expires = data.expires || 0;
    const isExpired = expires > 0 && now > expires;
    const secondsLeft = expires > 0 ? Math.max(0, Math.round((expires - now) / 1000)) : null;

    return {
      valid: !isExpired,
      isExpired,
      secondsLeft,
      memberId: data.memberId || data.id,
      name: data.name,
      plan: data.plan,
      gym: data.gym,
      issued: data.issued ? new Date(data.issued) : null,
      nonce: data.nonce || null,
    };
  } catch {
    return { valid: false, parseError: true, raw };
  }
};

const formatTime = (date) => {
  if (!date) return '—';
  return date.toLocaleTimeString('en-IN', { hour: '2-digit', minute: '2-digit', second: '2-digit' });
};

