// Returns today's workout based on day of week (0=Sun, 1=Mon...)
export const getTodaysWorkout = () => {
  const dayIndex = new Date().getDay(); // 0-6, Sun-Sat
  // Map JS day index to our schedule (Mon=0, Sun=6)
  const scheduleIndex = dayIndex === 0 ? 6 : dayIndex - 1;
  return workoutSchedule[scheduleIndex] || workoutSchedule[0];
};
